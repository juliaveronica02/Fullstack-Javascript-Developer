// if condition
if (7>5) {
    console.log("result", true);
}
// result if condition: true.

// if-else condition
if (5 > 7){
    console.log("true", true);
}
else {
    console.log("false", false);
}
// result if-else condition: false. so the true is being block.

// multiple condition (if, else if).
if (5 > 7){
    console.log("5 > 7"); //false.
}
else if (7 > 8) {
    console.log("7 >8"); // false.
}
else {
    console.log("nothing");
}
// result multiple condition (if, else if): nothing.