// arithmetic operators:
// 1. addition: "+".
// 2. Substraction:"-".
// 3. Multiplication: "*".
// 4. Division: "/".
// 5. modules: "%".
// 6. Increment: "++".
// 7. Decrement: "--".

var num1 = 10;
var num2 = 15;
var num3 = 20;

// addition.
console.log(num1 + num2);
console.log(num1 + num2 + num3);

// Substraction.
console.log(num2-num1);
console.log(num3 - num2 - num1);

// Multiplication.
console.log(num1 * num2);

// Division.
console.log(num2/num1);

// modulus.
console.log(num2 % num1);

// Increment post and pre.
// example 1.
console.log(num1++);
// example 2.
// console.log(++num1);
console.log(num1);

// Decrement post and pre.
// example 1.
console.log(num1--);
// example 2.
console.log(--num1);
console.log(num1);
