var myFirstString = "Javascript"
console.log("String", typeof(myFirstString));
// result: String string.

var mySecondString = 'Javascript'
console.log("String 2:", typeof(mySecondString));
// result: String 2: string.

// what if my string contains a double quote?.
var doubleQuotesString = "This is a \"Javascript\" string"
console.log("double string", doubleQuotesString);
// result: double string This is a "Javascript" string.

// what if my string contains a single quote?.
var singleQuotesString = "This is a \'Javascript\' string"
console.log("double string", singleQuotesString);
// result: double string This is a 'Javascript' string.